    private void signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupActionPerformed
        // TODO add your handling code here:
        String uid = id.getText();
        String username = name.getText();
        String date = dob.getText();
        String dept = (String)drop.getSelectedItem();
        String pwd = pass.getText();
        String pwd2 = confirm.getText();
        if(!pwd.equals(pwd2))
        {
            JOptionPane.showMessageDialog(null,"Passwords do not match");
        }
        String query;
        try
        {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/ims","root","12345");
                Statement stmt = con.createStatement();
                query = "Insert into admin(id,name,password,dept,dob)values("+uid+",'"+username+"','"+pwd+"','"+dept+"','"+date+"');";
                int rs=stmt.executeUpdate(query);
                if(rs==0)
                {
                    JOptionPane.showMessageDialog(null,"Admin Not Created");
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Created Successfully");
                    id.setText(null);
                    name.setText(null);
                    dob.setText(null);
                    pass.setText(null);
                    confirm.setText(null);
                }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_signupActionPerformed

